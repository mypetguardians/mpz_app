    raw = origin.strip()
    if not raw:
        continue
    # path가 포함된 경우 django-cors-headers가 에러를 내므로, 스킴+호스트(+포트)만 추출
    parsed = urlparse(raw)
    if parsed.scheme and parsed.netloc:
        cleaned = f"{parsed.scheme}://{parsed.netloc}"
    else:
        # 이미 스킴+호스트 형태라고 가정하고 그대로 사용
        cleaned = raw
    CORS_ALLOWED_ORIGINS.append(cleaned.lower())

